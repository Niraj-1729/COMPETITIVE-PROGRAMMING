 

import java.io.*;
import java.lang.*;
import java.util.*;

/* Name of the class has to be "Main" only if the class is public. */
class Codechef {

  public static void main(String[] args) throws java.lang.Exception {
    Scanner sc = new Scanner(System.in);
    int tree = sc.nextInt();
    while (tree-- > 0) {
      int a = sc.nextInt();
      int b = sc.nextInt();
      sc.close();
      System.out.println(a * b);
    }
  }
}
